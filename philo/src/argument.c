#include "../inc/philo.h"

int	ft_arg_check(int argc, char **argv)
{
	int	i;

	if (argc < 5 || 6 < argc)
		return (1);
	i = 1;
	while (i < argc)
	{
		if (ft_atoi(argv[i]) < 1)
			return (1);
		i++;
	}
	return (0);
}
